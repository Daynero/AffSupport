export type TranslationKey = keyof typeof en;
const dictionaries = { en, uk };
export const translationKeys = Object.keys(en) as TranslationKey[];

export function detectLanguage(saved: string | null, browserLanguages: readonly string[]): Language {
  if (saved === 'en' || saved === 'uk') return saved;
  return browserLanguages.some(value => value.toLowerCase().startsWith('uk')) ? 'uk' : 'en';
}

export function translate(
  language: Language,
  key: TranslationKey,
  values: Record<string, string | number> = {}
) {
  return Object.entries(values).reduce(
    (text, [name, value]) => text.replaceAll(`{${name}}`, String(value)),
    dictionaries[language][key]
  );
}

export function selectedCountKey(language: Language, count: number): TranslationKey {
  if (language === 'en') return count === 1 ? 'selectedOne' : 'selectedMany';
  const remainder10 = count % 10;
  const remainder100 = count % 100;
  return remainder10 === 1 && remainder100 !== 11 ? 'selectedOne' : 'selectedMany';
}

export function useI18n() {
  const [language, update] = useState<Language>(() =>
    detectLanguage(
      localStorage.getItem('language'),
      navigator.languages?.length ? navigator.languages : [navigator.language]
    )
  );
  useEffect(() => {
    localStorage.setItem('language', language);
    document.documentElement.lang = language;
  }, [language]);
  const t = useMemo(
    () => (key: TranslationKey, values?: Record<string, string | number>) =>
      translate(language, key, values),
    [language]
  );
  return { language, setLanguage: update, t };
}
